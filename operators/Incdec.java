public class Incdec {
    public static void inc_dec() {
        int a = 10;
        int b = 3;
        a++;
        b--;
        System.out.println("Increment: " + (a));
        System.out.println("Decrement: " + (b));
    }

    public static void main(String args[]) {
        inc_dec();
    }
}
